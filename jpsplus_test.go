var m AStarMatrix

func copyGrid() [][]uint8 {
	temp := make([][]uint8, len(grid))
	copy(temp, grid)
	for i, v := range grid {
		copy(temp[i], v)
	}
	return temp
}

func TestMain(t *testing.M) {
	for _, row := range strings.Split(strings.TrimSpace(world), "\n") {
		r := make([]uint8, 0, len(row))
		for _, raw := range row {
			n := 0
			if raw == 'X' {
				n = 1
			}
			r = append(r, uint8(n))
		}
		grid = append(grid, r)
	}
	m = NewMatrix(grid)
	t.Run()
}

func TestJump(t *testing.T) {
	// "o" is the jump path
	path, err := m.AStarJump([2]int64{0, 0}, [2]int64{49, 48})
	if err != nil {
		panic(err)
	}
	temp := copyGrid()
	for _, node := range path {
		temp[node.GetRow()][node.GetCol()] = 2
	}
	for _, v := range temp {
		for _, vv := range v {
			fmt.Printf("%s ", trans[vv])
		}
		fmt.Println()
	}
}

